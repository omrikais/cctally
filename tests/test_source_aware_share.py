def _period(ls):
    return ls.PeriodSpec(
        start=datetime(2026, 7, 1, tzinfo=UTC),
        end=datetime(2026, 7, 2, tzinfo=UTC),
        display_tz="UTC",
        label="Jul 01 → Jul 02 (UTC)",
    )


def _collision_snapshot(ls):
    return ls.ShareSnapshot(
        cmd="project",
        title="Projects",
        subtitle=None,
        period=_period(ls),
        columns=(
            ls.ColumnSpec(key="project", label="Project", kind="project"),
            ls.ColumnSpec(key="cost", label="$ Cost", align="right"),
        ),
        rows=(
            ls.Row(cells={
                "project": ls.ProjectCell("repo", rank_cost=1.0, identity="root-a"),
                "cost": ls.MoneyCell(1.0),
            }),
            ls.Row(cells={
                "project": ls.ProjectCell("repo", rank_cost=2.0, identity="root-b"),
                "cost": ls.MoneyCell(2.0),
            }),
        ),
        chart=ls.HorizontalBarChart(
            points=(
                ls.ChartPoint("repo", 0.0, 1.0, project_label="repo", project_identity="root-a"),
                ls.ChartPoint("repo", 1.0, 2.0, project_label="repo", project_identity="root-b"),
            ),
            x_label="$ Cost",
        ),
        totals=(),
        notes=(),
        generated_at=datetime(2026, 7, 2, 12, tzinfo=UTC),
        version="1.0.0",
    )


def test_equal_labels_with_distinct_qualified_identities_get_distinct_aliases():
    _ns, ls, _source = _kernel()

    scrubbed = ls._scrub(_collision_snapshot(ls), reveal_projects=False)

    row_labels = [row.cells["project"].label for row in scrubbed.rows]
    point_labels = [point.project_label for point in scrubbed.chart.points]
    assert set(row_labels) == {"project-1", "project-2"}
    assert point_labels == row_labels
    assert all(label != "repo" for label in point_labels)


