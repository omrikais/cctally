def test_codex_diff_share_keeps_each_native_row_status_and_ab_delta_metrics():
    ns, ls, source = _kernel()
    kernel = ns["_lib_source_analytics"]
    start = datetime(2026, 7, 1, tzinfo=UTC)
    middle = datetime(2026, 7, 1, 12, tzinfo=UTC)
    end = datetime(2026, 7, 2, tzinfo=UTC)
    entry = kernel.QualifiedCodexEntry
    entries = kernel.assign_collision_safe_project_labels((
        entry(
            timestamp=start, source_root_key="root-a", conversation_key="conversation-a",
            project_key="project:" + "a" * 24, project_label="repo", model="gpt-5",
            input_tokens=100, cached_input_tokens=20, output_tokens=30,
            reasoning_output_tokens=5, total_tokens=130, cost_usd=1.0,
        ),
        entry(
            timestamp=middle, source_root_key="root-b", conversation_key="conversation-b",
            project_key="project:" + "b" * 24, project_label="repo", model="gpt-5.5",
            input_tokens=200, cached_input_tokens=100, output_tokens=60,
            reasoning_output_tokens=10, total_tokens=260, cost_usd=3.0,
        ),
    ))
    result = kernel.build_codex_diff_result(
        entries,
        kernel.AnalyticsWindow("A", "calendar", start, middle),
        kernel.AnalyticsWindow("B", "calendar", middle, end),
        show_all=True,
        classify_presence=True,
    )

    snap = source.build_source_share_snapshot("diff", result, reveal_projects=False)
    scrubbed = ls._scrub(snap, reveal_projects=False)
    for fmt in ("md", "html", "svg"):
        rendered = ls.render(scrubbed, format=fmt, theme="light", branding=True)
        metrics = (
            ("A $", "B $", "Δ $", "Cost", "A", "B", "Δ", "Tokens")
            if fmt == "svg" else
            ("A $ Cost", "B $ Cost", "Δ $ Cost", "A Tokens", "B Tokens", "Δ Tokens")
        )
        labels = ("project-",) if fmt == "svg" else ("project-1", "project-2")
        for text in (*metrics, "gpt-5", "gpt-5.5", "dropped", "new", *labels):
            assert text in rendered
        assert "root-a" not in rendered and "root-b" not in rendered


