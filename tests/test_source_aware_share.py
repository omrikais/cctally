def test_source_aware_codex_report_goldens_are_plain_under_ci_color_env(tmp_path):
    """Codex-report terminal goldens must stay ANSI-free even when the ambient
    environment forces color. GitHub Actions sets $CI, which
    _supports_color_stdout() treats as color-on; the Codex-report handlers take
    no color arg (unlike the source-family), so the builder must neutralize
    color itself. Without that, the committed plain goldens diverge on CI —
    the exact regression this guards. Non-vacuous: building under CI=1/
    FORCE_COLOR=1 without the NO_COLOR pin re-introduces the \\x1b codes and
    fails the byte-compare below.
    """
    root = Path(__file__).resolve().parents[1]
    builder = root / "bin" / "build-source-aware-fixtures.py"
    out = tmp_path / "source-aware"

    env = {**os.environ, "CI": "1", "FORCE_COLOR": "1"}
    result = subprocess.run(
        [sys.executable, str(builder), "--out", str(out)],
        text=True, capture_output=True, check=False, env=env,
    )
    assert result.returncode == 0, result.stderr

    committed = root / "tests" / "fixtures" / "source-aware"
    for command in ("codex-daily", "codex-monthly", "codex-weekly", "codex-session"):
        rel = Path(f"codex-report-{command}-populated") / "output.terminal.golden"
        generated = (out / rel).read_bytes()
        assert b"\x1b" not in generated, f"{rel} gained ANSI codes under CI color env"
        assert generated == (committed / rel).read_bytes(), rel


