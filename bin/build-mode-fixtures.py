#!/usr/bin/env python3
"""Build the seeded mixed-``costUSD`` fixture for the ``--mode`` reconcile
invariants (issue #86 Session C).

Writes one fixture dir under ``tests/fixtures/mode/<scenario>/``. Each dir
contains the standard fake-HOME layout the reconcile harness consumes:
  - .local/share/cctally/stats.db (empty snapshots — cost comes from cache)
  - .local/share/cctally/cache.db (seeded session_entries; MIXED cost_usd_raw)
  - .claude/projects/ (empty — cache is pre-seeded, no JSONL scan needed)
  - input.env (AS_OF for the harness)
  - .gitignore (per fixture-scoped-gitignore memory)

Run: ``bin/build-mode-fixtures.py`` (idempotent; overwrites DBs).

Why a dedicated fixture (NOT a mutation of an existing one)
-----------------------------------------------------------
The three cost-source modes (``auto`` / ``calculate`` / ``display``) only
diverge when SOME entries carry a recorded ``costUSD`` (``cache.db``
``session_entries.cost_usd_raw``) that differs from the pricing-computed
cost and OTHERS carry ``NULL``. Existing golden fixtures deliberately carry
NO recorded cost (so their default-``auto`` goldens stay byte-stable), which
makes them useless for proving the modes diverge. This builder seeds the
mixed shape used ONLY by the reconcile invariants (§4.1).

Scenario: ``mixed-cost``
------------------------
One subscription week, two calendar days, two models. Each day has one
entry WITH a recorded ``cost_usd_raw`` (deliberately != its computed cost)
and one entry WITHOUT (``NULL``). This guarantees:
  * ``display`` total < ``auto`` total  (the NULL-cost entries drop to $0)
  * ``auto`` total   != ``calculate`` total  (recorded != computed on the
    WITH-cost entries, so auto reuses recorded while calculate recomputes)
  * ``calculate`` total is token-only (invariant to ``cost_usd_raw`` presence)
"""
from __future__ import annotations

import argparse
import datetime as dt
import sqlite3
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from _fixture_builders import (  # noqa: E402
    create_cache_db,
    create_stats_db,
    seed_session_entry,
    seed_session_file,
)

FIXTURES_DIR = Path(__file__).resolve().parent.parent / "tests/fixtures/mode"
GITIGNORE_BODY = """\
# Generated by bin/build-mode-fixtures.py
config.json
lock
*.log
*.db-wal
*.db-shm
"""

# Two models so the fixture exercises ≥2 distinct pricing rows. Recorded
# cost_usd_raw values are chosen to dwarf the computed token cost so the
# auto-vs-calculate divergence is unambiguous (and never collides with a
# computed value through float rounding).
MODEL_A = "claude-opus-4-7"
MODEL_B = "claude-sonnet-4-6"

# Recorded costUSD values, deliberately != the pricing-computed cost for
# the same token counts (computed cost for these token counts is well
# under $1; these recorded values are large + arbitrary).
RECORDED_COST_DAY1 = 9.99   # on the MODEL_A entry, day 1
RECORDED_COST_DAY2 = 5.50   # on the MODEL_B entry, day 2


def _iso(d: dt.datetime) -> str:
    return d.astimezone(dt.timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _fixture_tree(name: str) -> tuple[Path, Path, Path]:
    base = FIXTURES_DIR / name
    share = base / ".local" / "share" / "cctally"
    projects = base / ".claude" / "projects"
    share.mkdir(parents=True, exist_ok=True)
    projects.mkdir(parents=True, exist_ok=True)
    return base, share, projects


def _write_input_env(base: Path, as_of: str) -> None:
    (base / "input.env").write_text(f"AS_OF={as_of}\n")
    (base / ".gitignore").write_text(GITIGNORE_BODY)


def build_mixed_cost() -> None:
    """One week, two days, two models; mixed recorded/absent costUSD."""
    base, share, _ = _fixture_tree("mixed-cost")

    # Subscription week: 2026-04-20 (Mon) → 2026-04-27. Two entries on
    # 2026-04-21, two on 2026-04-23. AS_OF sits a few hours after the
    # last entry, inside the same week, so `weekly`'s current-week rollup
    # reads the seeded entries directly (not the extrapolation tail).
    day1 = dt.datetime(2026, 4, 21, 10, 0, tzinfo=dt.timezone.utc)
    day2 = dt.datetime(2026, 4, 23, 14, 0, tzinfo=dt.timezone.utc)
    as_of = dt.datetime(2026, 4, 23, 20, 0, tzinfo=dt.timezone.utc)
    _write_input_env(base, _iso(as_of))

    create_stats_db(share / "stats.db")
    create_cache_db(share / "cache.db")

    with sqlite3.connect(share / "cache.db") as cc:
        seed_session_file(
            cc,
            path="/fx/mode.jsonl",
            session_id="s-mode",
            project_path="/fx",
        )
        # Day 1, MODEL_A: recorded costUSD present (!= computed).
        seed_session_entry(
            cc,
            source_path="/fx/mode.jsonl",
            line_offset=0,
            timestamp_utc=_iso(day1),
            model=MODEL_A,
            input_tokens=1000,
            output_tokens=500,
            cost_usd_raw=RECORDED_COST_DAY1,
        )
        # Day 1, MODEL_B: NO recorded costUSD (NULL).
        seed_session_entry(
            cc,
            source_path="/fx/mode.jsonl",
            line_offset=1,
            timestamp_utc=_iso(day1 + dt.timedelta(minutes=30)),
            model=MODEL_B,
            input_tokens=2000,
            output_tokens=800,
            cost_usd_raw=None,
        )
        # Day 2, MODEL_A: NO recorded costUSD (NULL).
        seed_session_entry(
            cc,
            source_path="/fx/mode.jsonl",
            line_offset=2,
            timestamp_utc=_iso(day2),
            model=MODEL_A,
            input_tokens=1500,
            output_tokens=600,
            cost_usd_raw=None,
        )
        # Day 2, MODEL_B: recorded costUSD present (!= computed).
        seed_session_entry(
            cc,
            source_path="/fx/mode.jsonl",
            line_offset=3,
            timestamp_utc=_iso(day2 + dt.timedelta(minutes=45)),
            model=MODEL_B,
            input_tokens=3000,
            output_tokens=1200,
            cost_usd_raw=RECORDED_COST_DAY2,
        )


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--out",
        type=Path,
        default=None,
        help=(
            "Override output directory. Defaults to the in-tree path "
            "tests/fixtures/mode/. Used by bin/cctally-reconcile-test "
            "to write into a per-run scratch dir so the in-tree fixtures "
            "stay byte-stable across harness runs."
        ),
    )
    args = parser.parse_args()
    if args.out is not None:
        FIXTURES_DIR = args.out
    FIXTURES_DIR.mkdir(parents=True, exist_ok=True)
    for builder in (build_mixed_cost,):
        builder()
    print(f"Built fixtures under {FIXTURES_DIR}")
