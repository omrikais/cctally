#!/usr/bin/env python3
"""Build seeded fixtures for `cctally blocks`.

Writes one fixture dir per scenario under
`tests/fixtures/blocks/<scenario>/`. Each dir contains:
  - .local/share/cctally/stats.db (seeded snapshots)
  - .local/share/cctally/cache.db (seeded session entries)
  - .claude/projects/ (empty — cache is pre-seeded, no JSONL scan needed)
  - input.env (AS_OF for the harness)
  - .gitignore (per fixture-scoped-gitignore memory)

Run: `bin/build-blocks-fixtures.py` (idempotent; overwrites DBs).

Scenarios
---------
1. web-initiated     — user started 5h window on claude.ai web ~50min
                       before first CC entry; recorded R is earlier than
                       floor(first_cc_entry)+5h.
2. cc-only           — CC-initiated window; R-5h aligns with
                       floor(first_cc_entry).
3. heuristic-fallback — JSONL entries only, NO weekly_usage_snapshots
                       rows → heuristic block with tilde.
4. mixed             — earliest entries before any recorded snapshot;
                       later entries inside recorded window.
5. active-window     — AS_OF inside a recorded window; ACTIVE row with
                       real remaining time.
"""
from __future__ import annotations

import argparse
import datetime as dt
import sqlite3
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from _fixture_builders import (  # noqa: E402
    create_cache_db,
    create_stats_db,
    seed_session_entry,
    seed_session_file,
    seed_weekly_usage_snapshot,
)

FIXTURES_DIR = Path(__file__).resolve().parent.parent / "tests/fixtures/blocks"
FIXED_MODEL = "claude-sonnet-4-6"
GITIGNORE_BODY = """\
# Generated by bin/build-blocks-fixtures.py
config.json
lock
*.log
*.db-wal
*.db-shm
"""


def _iso(d: dt.datetime) -> str:
    return d.astimezone(dt.timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _fixture_tree(name: str) -> tuple[Path, Path, Path]:
    base = FIXTURES_DIR / name
    share = base / ".local" / "share" / "cctally"
    projects = base / ".claude" / "projects"
    share.mkdir(parents=True, exist_ok=True)
    projects.mkdir(parents=True, exist_ok=True)
    return base, share, projects


def _write_input_env(base: Path, as_of: str) -> None:
    (base / "input.env").write_text(f"AS_OF={as_of}\n")
    (base / ".gitignore").write_text(GITIGNORE_BODY)


def _seed_week_snapshot(
    stats_conn: sqlite3.Connection,
    *,
    R: dt.datetime,
    captured_at: dt.datetime,
    weekly_percent: float,
    five_hour_percent: float,
) -> None:
    """Seed a weekly_usage_snapshot carrying a 5h reset.

    Uses a dummy week window bracketing `captured_at`. Exercises the
    ISO-timestamp week path so `blocks` picks up `five_hour_resets_at`
    via the recorded-windows query."""
    week_start = captured_at.replace(hour=0, minute=0, second=0, microsecond=0) - dt.timedelta(days=3)
    week_end = week_start + dt.timedelta(days=7)
    seed_weekly_usage_snapshot(
        stats_conn,
        captured_at_utc=_iso(captured_at),
        week_start_date=week_start.date().isoformat(),
        week_end_date=week_end.date().isoformat(),
        week_start_at=_iso(week_start),
        week_end_at=_iso(week_end),
        weekly_percent=weekly_percent,
        five_hour_percent=five_hour_percent,
        five_hour_resets_at=_iso(R),
        source="fixture",
        payload_json='{"fixture": true}',
    )


def _seed_entries(
    cache_conn: sqlite3.Connection,
    entries: list[tuple[dt.datetime, int, int]],
) -> None:
    """entries: list of (timestamp, input_tokens, output_tokens)."""
    seed_session_file(
        cache_conn,
        path="/fx/blocks.jsonl",
        session_id="s-blocks",
        project_path="/fx",
    )
    for i, (ts, inp, outp) in enumerate(entries):
        seed_session_entry(
            cache_conn,
            source_path="/fx/blocks.jsonl",
            line_offset=i,
            timestamp_utc=_iso(ts),
            model=FIXED_MODEL,
            input_tokens=inp,
            output_tokens=outp,
        )


# -- Scenario 1: web-initiated -------------------------------------------
def build_web_initiated() -> None:
    """User started 5h window on claude.ai web ~50min before first CC entry.
    Recorded R is earlier than floor(first_cc_entry)+5h.

    R is deliberately off-hour (11:30:00Z) to mirror real Anthropic reset
    jitter and ensure the recorded anchor diverges from the heuristic
    floor(first_cc)+5h. With R=11:30 and first_cc=07:20:
      - recorded window: [06:30, 11:30)
      - heuristic would be: [07:00, 12:00)
    The two anchor schemes are distinct, so this scenario actually
    exercises the recorded-anchor branch (not equivalent to cc-only)."""
    base, share, _ = _fixture_tree("web-initiated")
    R = dt.datetime(2026, 4, 23, 11, 30, tzinfo=dt.timezone.utc)
    first_cc = R - dt.timedelta(hours=4, minutes=10)  # = 07:20:00Z
    as_of = R - dt.timedelta(hours=1)  # = 10:30:00Z, inside [06:30, 11:30)
    _write_input_env(base, _iso(as_of))
    create_stats_db(share / "stats.db")
    create_cache_db(share / "cache.db")
    with sqlite3.connect(share / "stats.db") as sc:
        _seed_week_snapshot(
            sc,
            R=R,
            captured_at=first_cc,
            weekly_percent=35.0,
            five_hour_percent=20.0,
        )
    with sqlite3.connect(share / "cache.db") as cc:
        _seed_entries(cc, [
            (first_cc, 1000, 2000),
            (first_cc + dt.timedelta(minutes=20), 800, 1500),
        ])


# -- Scenario 2: cc-only -------------------------------------------------
def build_cc_only() -> None:
    """CC-initiated window: R-5h aligns with floor(first_cc_entry)."""
    base, share, _ = _fixture_tree("cc-only")
    first_cc = dt.datetime(2026, 4, 23, 7, 0, tzinfo=dt.timezone.utc)  # exact hour
    R = first_cc + dt.timedelta(hours=5)
    as_of = R - dt.timedelta(hours=1)
    _write_input_env(base, _iso(as_of))
    create_stats_db(share / "stats.db")
    create_cache_db(share / "cache.db")
    with sqlite3.connect(share / "stats.db") as sc:
        _seed_week_snapshot(
            sc,
            R=R,
            captured_at=first_cc + dt.timedelta(minutes=5),
            weekly_percent=22.0,
            five_hour_percent=10.0,
        )
    with sqlite3.connect(share / "cache.db") as cc:
        _seed_entries(cc, [
            (first_cc, 500, 1000),
            (first_cc + dt.timedelta(hours=2), 300, 600),
        ])


# -- Scenario 3: heuristic-fallback --------------------------------------
def build_heuristic_fallback() -> None:
    """JSONL entries only, NO weekly_usage_snapshots rows → heuristic with tilde."""
    base, share, _ = _fixture_tree("heuristic-fallback")
    first_cc = dt.datetime(2026, 4, 22, 15, 30, tzinfo=dt.timezone.utc)
    as_of = first_cc + dt.timedelta(hours=2)
    _write_input_env(base, _iso(as_of))
    create_stats_db(share / "stats.db")  # empty snapshots table
    create_cache_db(share / "cache.db")
    with sqlite3.connect(share / "cache.db") as cc:
        _seed_entries(cc, [
            (first_cc, 1000, 2000),
            (first_cc + dt.timedelta(minutes=30), 500, 900),
        ])


# -- Scenario 4: mixed ---------------------------------------------------
def build_mixed() -> None:
    """Earliest entries before any recorded snapshot; later entries inside recorded window."""
    base, share, _ = _fixture_tree("mixed")
    pre_R_entry = dt.datetime(2026, 4, 21, 9, 0, tzinfo=dt.timezone.utc)
    R = dt.datetime(2026, 4, 23, 12, 0, tzinfo=dt.timezone.utc)
    recorded_entry = R - dt.timedelta(hours=2)
    as_of = R - dt.timedelta(hours=1)
    _write_input_env(base, _iso(as_of))
    create_stats_db(share / "stats.db")
    create_cache_db(share / "cache.db")
    with sqlite3.connect(share / "stats.db") as sc:
        _seed_week_snapshot(
            sc,
            R=R,
            captured_at=recorded_entry + dt.timedelta(minutes=5),
            weekly_percent=40.0,
            five_hour_percent=25.0,
        )
    with sqlite3.connect(share / "cache.db") as cc:
        _seed_entries(cc, [
            (pre_R_entry, 800, 1200),
            (recorded_entry, 600, 1000),
        ])


# -- Scenario 5: active-window -------------------------------------------
def build_active_window() -> None:
    """AS_OF inside a recorded window; ACTIVE row with real remaining time."""
    base, share, _ = _fixture_tree("active-window")
    R = dt.datetime(2026, 4, 23, 14, 0, tzinfo=dt.timezone.utc)
    first_cc = R - dt.timedelta(hours=2)  # 3h before R
    as_of = R - dt.timedelta(hours=1, minutes=15)  # "now" mid-session
    _write_input_env(base, _iso(as_of))
    create_stats_db(share / "stats.db")
    create_cache_db(share / "cache.db")
    with sqlite3.connect(share / "stats.db") as sc:
        _seed_week_snapshot(
            sc,
            R=R,
            captured_at=first_cc + dt.timedelta(minutes=10),
            weekly_percent=55.0,
            five_hour_percent=45.0,
        )
    with sqlite3.connect(share / "cache.db") as cc:
        _seed_entries(cc, [
            (first_cc, 2000, 3000),
            (first_cc + dt.timedelta(minutes=30), 1500, 2500),
        ])


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--out",
        type=Path,
        default=None,
        help=(
            "Override output directory. Defaults to the in-tree path "
            "tests/fixtures/blocks/. Used by cctally-blocks-test "
            "to write into a per-run scratch dir so the in-tree fixtures "
            "stay byte-stable across harness runs."
        ),
    )
    args = parser.parse_args()
    if args.out is not None:
        FIXTURES_DIR = args.out
    FIXTURES_DIR.mkdir(parents=True, exist_ok=True)
    for builder in (
        build_web_initiated,
        build_cc_only,
        build_heuristic_fallback,
        build_mixed,
        build_active_window,
    ):
        builder()
    print(f"Built fixtures under {FIXTURES_DIR}")
