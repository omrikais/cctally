FIXTURES_DIR = Path(__file__).resolve().parent.parent / "tests/fixtures/blocks"
FIXED_MODEL = "claude-sonnet-4-6"
GITIGNORE_BODY = """\
# Generated by bin/build-blocks-fixtures.py
config.json
lock
*.log
*.db-wal
*.db-shm
"""


def _iso(d: dt.datetime) -> str:
    return d.astimezone(dt.timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _fixture_tree(name: str) -> tuple[Path, Path, Path]:
    base = FIXTURES_DIR / name
    share = base / ".local" / "share" / "cctally"
    projects = base / ".claude" / "projects"
    share.mkdir(parents=True, exist_ok=True)
    projects.mkdir(parents=True, exist_ok=True)
    return base, share, projects


def _write_input_env(base: Path, as_of: str, *, flags: str | None = None) -> None:
    lines = [f"AS_OF={as_of}\n"]
    if flags:
        # The shared `_lib-fixture-harness.sh` reads FLAGS and merges it into
        # every run_mode call (`merged_flags="$flags $FLAGS"`), so the flags
        # apply to BOTH the terminal and --json modes of this fixture.
        # Quote the value (matching the cache-report/codex-daily/diff fixtures'
        # convention) so a multi-token FLAGS like `-a -t 100000` survives the
        # `. input.env` source — an unquoted `FLAGS=-a -t 100000` would assign
        # only `FLAGS=-a` and then try to run `-t` as a command.
        lines.append(f'FLAGS="{flags}"\n')
    (base / "input.env").write_text("".join(lines))
    (base / ".gitignore").write_text(GITIGNORE_BODY)


def _seed_week_snapshot(
    stats_conn: sqlite3.Connection,
    *,
    R: dt.datetime,
    captured_at: dt.datetime,
    weekly_percent: float,
    five_hour_percent: float,
) -> None:
    """Seed a weekly_usage_snapshot carrying a 5h reset.

    Uses a dummy week window bracketing `captured_at`. Exercises the
    ISO-timestamp week path so `blocks` picks up `five_hour_resets_at`
    via the recorded-windows query."""
    week_start = captured_at.replace(hour=0, minute=0, second=0, microsecond=0) - dt.timedelta(days=3)
    week_end = week_start + dt.timedelta(days=7)
    seed_weekly_usage_snapshot(
        stats_conn,
        captured_at_utc=_iso(captured_at),
        week_start_date=week_start.date().isoformat(),
        week_end_date=week_end.date().isoformat(),
        week_start_at=_iso(week_start),
        week_end_at=_iso(week_end),
        weekly_percent=weekly_percent,
        five_hour_percent=five_hour_percent,
        five_hour_resets_at=_iso(R),
        source="fixture",
        payload_json='{"fixture": true}',
    )


def _seed_entries(
    cache_conn: sqlite3.Connection,
    entries: list[tuple[dt.datetime, int, int]],
) -> None:
    """entries: list of (timestamp, input_tokens, output_tokens)."""
    seed_session_file(
        cache_conn,
        path="/fx/blocks.jsonl",
        session_id="s-blocks",
        project_path="/fx",
    )
    for i, (ts, inp, outp) in enumerate(entries):
        seed_session_entry(
            cache_conn,
            source_path="/fx/blocks.jsonl",
            line_offset=i,
            timestamp_utc=_iso(ts),
            model=FIXED_MODEL,
            input_tokens=inp,
            output_tokens=outp,
        )


# -- Scenario 1: web-initiated -------------------------------------------
def build_web_initiated() -> None:
    """User started 5h window on claude.ai web ~50min before first CC entry.
    Recorded R is earlier than floor(first_cc_entry)+5h.

    R is deliberately off-hour (11:30:00Z) to mirror real Anthropic reset
    jitter and ensure the recorded anchor diverges from the heuristic
    floor(first_cc)+5h. With R=11:30 and first_cc=07:20:
      - recorded window: [06:30, 11:30)
      - heuristic would be: [07:00, 12:00)
    The two anchor schemes are distinct, so this scenario actually
    exercises the recorded-anchor branch (not equivalent to cc-only)."""
    base, share, _ = _fixture_tree("web-initiated")
    R = dt.datetime(2026, 4, 23, 11, 30, tzinfo=dt.timezone.utc)
    first_cc = R - dt.timedelta(hours=4, minutes=10)  # = 07:20:00Z
    as_of = R - dt.timedelta(hours=1)  # = 10:30:00Z, inside [06:30, 11:30)
    _write_input_env(base, _iso(as_of))
    create_stats_db(share / "stats.db")
    create_cache_db(share / "cache.db")
    with sqlite3.connect(share / "stats.db") as sc:
        _seed_week_snapshot(
            sc,
            R=R,
            captured_at=first_cc,
            weekly_percent=35.0,
            five_hour_percent=20.0,
        )
    with sqlite3.connect(share / "cache.db") as cc:
        _seed_entries(cc, [
            (first_cc, 1000, 2000),
            (first_cc + dt.timedelta(minutes=20), 800, 1500),
        ])


